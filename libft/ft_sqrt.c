#include "libft.h"

int	ft_sqrt(int n)
{
	int i;

	if (n <= 0)
		return (0);
	else if (n == 1)
		return (1);
	while (n > 1)
	{
		i = 0;
		while (i <= n / 2)
		{
			if (i * i == n)
				return (i);
			i++;
		}
		n--;
	}
	return (0);
}
